// نختار سؤال وتحدي بناءً على تاريخ الكبسولة وid العائلة (deterministic)
function hashStr(s: string): number {
  let h = 0;
  for (let i = 0; i < s.length; i++) {
    h = (h * 31 + s.charCodeAt(i)) >>> 0;
  }
  return h;
}

export function getDailyContent(familyId: string, dateISO: string) {
  const baseHash = hashStr(`${familyId}-${dateISO}`);
  const question = QUESTIONS[baseHash % QUESTIONS.length];
  const challenge = CHALLENGES[hashStr(`c-${familyId}-${dateISO}`) % CHALLENGES.length];
  return { question, challenge };
}

export function todayISO(): string {
  const d = new Date();
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, "0");
  const day = String(d.getDate()).padStart(2, "0");
  return `${y}-${m}-${day}`;
}

export function formatArabicDate(iso: string): string {
  const date = new Date(iso + "T00:00:00");
  return date.toLocaleDateString("ar-EG", {
    weekday: "long",
    year: "numeric",
    month: "long",
    day: "numeric",
  });
}
